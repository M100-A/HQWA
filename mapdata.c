// function to increment the usage count for a link id.
//
int mapdata_printLinkVisitValue (short sVisitVal)
{
    int iRet;

    switch (sVisitVal)
    {
    case 0: // north
        iRet = mutils_addToDialogBuffer ("an unknown ");
        break;

    case 1: // east
        iRet = mutils_addToDialogBuffer ("a once-used ");
        break;

    case 2: // south
        iRet = mutils_addToDialogBuffer ("a familiar ");
        break;

    default: // case 3: west
        iRet = mutils_addToDialogBuffer ("a well-traveled ");
        break;
    }

    return iRet;
}
